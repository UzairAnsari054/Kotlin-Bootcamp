package com.example.kotlinbootcamp.basics.more_basics

// Nested Class is a class created inside another class
// Nested Class by default is Static , so it's data members and member functions
// can be accessed without creating the class object
// Nested class cannot access the data member of outer class

// Inner Class is a class created inside another class with keyword inner
// Inner Class cannot be declared inside Interface
// Benefit of Inner CLass is that it can access  the data member of its outer class
// even when it's private