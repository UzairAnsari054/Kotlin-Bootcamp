package com.example.kotlinbootcamp.basics.more_basics

// Collections are simply a group of same type of data or different types of data
// Kotlin has 2 types of Collection Classes
// 1. Collection of same type of elements
// 2. Collection of same or different types of elements

// 1. Collection of same type of elements (Examples)
// IntArray - Integer
// BooleanArray - Boolean
// DoubleArray - Double
// ByteArray - Byte
// LongArray - Long
// ShortArray - Short
//  FloatArray - Float

// 2. Collection of same or different types of elements (Examples)
// arrayOf<String>
// array("Fruit", 12, 1.0 )

// Immutable Collections - Read Only ()
// List - listOf
// Set - setOf
// Map - mapOf


// Mutable Collections - Read Only ()
// Mutable List - ArrayList, arrayListOf, mutableListOf
// Mutable Set - mutableSetOf, hashSetOf
// Mutable Map - HashMap, hashMapOf, mutableMapOf